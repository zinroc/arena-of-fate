module.exports = crowdFactory = {
    /**
     * Create a new crowd for given arena
     */
    generateCrowd: function (arena) {
        var numPeople = arena.capacity,
            crowd = [];
        for (var i = 0; i < numPeople; i++) {
            crowd[i] = crowdFactory.create (arena);
        }
        return crowd;
    },
    /**
     * Create single crowd member
     */
    create: function (arena) {
        return {
            home_arena: arena.id
        };
    }
};
